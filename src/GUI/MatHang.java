    public void loadMathangList(){
        DefaultTableModel dtm = (DefaultTableModel) tb_sanpham.getModel();
        dtm.setRowCount(0);
        arr = mathangBUS.getAllMathang();
        for (int i = 0; i < arr.size(); i++) {
            int mamh = arr.get(i).getMaMatmang();
            String tenmh = arr.get(i).getTenMathang();
            int mancc = arr.get(i).getMaNhacungcap();
            Object[] row = {mamh,tenmh,mancc};
            dtm.addRow(row);
        }
    }

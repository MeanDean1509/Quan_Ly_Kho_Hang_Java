    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
        // TODO add your handling code here:
        try{
            if (txt_mamathang.getText().trim().equals("")||
                txt_tenmathang.getText().trim().equals("")||
                txt_mancc.getText().trim().equals(""))
                JOptionPane.showConfirmDialog(this, "Vui long nhap du thong tin");
            else {
                DTO_mathang mh = new DTO_mathang();
                mh.setMaMatmang(Integer.parseInt(txt_mamathang.getText()));
                mh.setTenMathang(txt_tenmathang.getText());
                mh.setMaNhacungcap(Integer.parseInt(txt_mancc.getText()));
                JOptionPane.showMessageDialog(this, mathangBUS.addMathang(mh));
                loadMathangList();
            }
        } catch (NumberFormatException ex){
            JOptionPane.showMessageDialog(this, "Thong tin khong hop le");
        }
    }//GEN-LAST:event_btn_themActionPerformed

